<script>
export default {
  data () {
    return {
      page: 0,
      size: 10,
      dataList: []
    }
  },
  onShow () {
    this.dataList = []
    wx.setNavigationBarTitle({
      title: '历史订单'
    })
    wx.setNavigationBarColor({
      frontColor: '#ffffff', // 前景颜色值，包括按钮、标题、状态栏的颜色，仅支持 #ffffff 和 #000000 (微信小程序官方规定)
      backgroundColor: '#48B152'// 背景颜色值，有效值为十六进制颜色
    })
    this.scrolltolower()
  },
  methods: {
    scrolltolower: function () {
      this.page++
      this.$http.get('/api/orderDetail/list', {page: this.page, size: this.size}).then((res) => { // 产品素材接口
        if (res.data == '') {
          this.page--
          wx.showToast({
            title: '已经是全部数据了',
            icon: 'success',
            duration: 2000
          })
        } else {
          var data = res.data
          for (let index = 0; index < data.length; index++) {
            // console.log(this.timestampToTime (1551144627000))
            this.dataList.push(data[index])
          }
          // console.log(this.dataList)
        }
      })
    }
  }
}
